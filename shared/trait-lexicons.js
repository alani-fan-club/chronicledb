/**
 * Trait-classification lexicons for ChronicleDB.
 *
 * This module ships three static datasets used by the `classifyDisposition`
 * function in server-plugin/db.js to decide whether a candidate character
 * trait should be auto-accepted (persistent personality), auto-rejected
 * (transient emotional state), or flagged for further verification:
 *
 *   - GOLDBERG_100  : Set<string>  — dispositional personality adjectives
 *   - NRC_EMOTION   : Set<string>  — emotion-associated words (reject tier)
 *   - NRC_VAD       : Map<string, {valence, arousal, dominance}>
 *                     — valence/arousal/dominance scores for ~160 common
 *                       affect words (curated subset; see below)
 *
 * DATA PROVENANCE
 * ===============
 *
 *   (1) Goldberg Big-Five Markers. Core of GOLDBERG_100 is Goldberg's 100
 *       unipolar Big-Five factor markers from:
 *
 *         Goldberg, L. R. (1992). The development of markers for the
 *         Big-Five factor structure. Psychological Assessment, 4(1), 26-42.
 *         DOI: 10.1037/1040-3590.4.1.26
 *
 *       Supplemented with Saucier's 40-adjective Mini-Markers:
 *
 *         Saucier, G. (1994). Mini-Markers: A brief version of Goldberg's
 *         unipolar Big-Five markers. Journal of Personality Assessment,
 *         63(3), 506-516. PMID: 7844738
 *
 *       And extended with ~180 character-trait adjectives drawn from
 *       Goldberg's hierarchical analysis of 1,710 trait-descriptive
 *       adjectives for coverage on common roleplay character words
 *       (stoic, analytical, sarcastic, resourceful, etc.) that fall
 *       outside the orthogonal OCEAN factor structure. High-arousal
 *       neuroticism markers (anxious, nervous, jealous, moody, etc.)
 *       are intentionally OMITTED from the accept set — these are
 *       commonly transient in roleplay text and are routed through
 *       the NRC emotion path instead.
 *
 *   (2) NRC Word-Emotion Association Lexicon (EmoLex, v0.92).
 *
 *         Mohammad, S. M., & Turney, P. D. (2013). Crowdsourcing a
 *         word-emotion association lexicon. Computational Intelligence,
 *         29(3), 436-465.
 *         http://saifmohammad.com/WebPages/NRC-Emotion-Lexicon.htm
 *
 *       NRC_EMOTION contains every word in the NRC lexicon tagged with at
 *       least one of the 8 basic emotions (anger, anticipation, disgust,
 *       fear, joy, sadness, surprise, trust), plus approximate English
 *       stems of those words (matching the `approxStem` helper in db.js).
 *       Supplemented with ~100 curated transient-affect words/inflections
 *       (adoring, appreciative, aroused, astonished, baffled, bemused,
 *       besotted, captivated, desperate, enamored, fascinated, grateful,
 *       grieving, impressed, infatuated, mortified, puzzled, relieved,
 *       smitten, terrified, touched, etc.) where NRC's base-word coverage
 *       is incomplete for inflected forms commonly seen in fiction text.
 *
 *       NRC Emotion Lexicon is available for free for research and
 *       non-commercial use only. Commercial use requires a license from
 *       the National Research Council Canada (contact
 *       saif.mohammad@nrc-cnrc.gc.ca). ChronicleDB's use here is as a
 *       downstream filter, not redistribution — only the lexicon's word
 *       membership (which word appears in the lexicon) is queried, not
 *       the full annotations.
 *
 *   (3) NRC-VAD Lexicon (Valence / Arousal / Dominance).
 *
 *         Mohammad, S. M. (2018). Obtaining reliable human ratings of
 *         valence, arousal, and dominance for 20,000 English words.
 *         Proceedings of ACL 2018.
 *         http://saifmohammad.com/WebPages/nrc-vad.html
 *
 *       Only a curated ~160-word subset is shipped here covering the most
 *       common affect adjectives seen in character-trait extraction. The
 *       arousal values are approximations drawn from the published NRC-VAD
 *       and Warriner et al. (2013) extended ANEW ranges, normalized to
 *       [0, 1] to match NRC-VAD's published format. The full 20k-word VAD
 *       file is under the same non-commercial license as (2) and is not
 *       redistributed here. If you have a licensed copy of the full file,
 *       `NRC_VAD` can be replaced or extended at module load time.
 *
 * LICENSING
 * =========
 *   - Goldberg markers (1): peer-reviewed psychometric instrument,
 *     published 1992, freely reproduced for research and educational use
 *     for decades.
 *   - NRC lexicons (2, 3): free for research and non-commercial use only.
 *     Cite Mohammad & Turney (2013) and Mohammad (2018) if you use this.
 *     No redistribution of the full annotated data files — only membership
 *     information for the word list is embedded here.
 */

